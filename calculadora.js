function soma(a, b) {
    return a + b;
}

function subtração(a, b) {
    return a - b;
}

function multiplicação(a, b) {
    return a * b;
}

function divisão(a, b) {
    return a / b;
}

console.log("Soma: " + soma(5, 3));
console.log("Subtração: " + subtração(5, 3));
console.log("Multiplicação: " + multiplicação(5, 3));
console.log("Divisão: " + divisão(6, 3));